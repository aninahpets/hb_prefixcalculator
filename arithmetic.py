def add(num1, num2):
    total = num1 + num2
    return total

def subtract(num1, num2):
    total = num1 - num2
    return total

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    total = float(num1) / float(num2)
    return total

def square(num1):
    return num1 * num1

def cube(num1):
    return num1 * num1 * num1

def power(num1, num2):
    return num1 ** num2

def mod(num1, num2):
    return num1 % num2
