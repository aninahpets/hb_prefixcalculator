def add(num1, num2):
    total = num1 + num2
    return total

def subtract(num1, num2):
    total = num1 - num2
    return total

def multiply(num1, num2):
    total = num1 * num2
    return total

def divide(num1, num2):
    total = float(num1) / float(num2)
    return total

def square(num1):
    total = num1 * num1
    return total

def cube(num1):
    total = num1 * num1 * num1
    return total

def power(num1, num2):
    total = num1 ** num2
    return total

def mod(num1, num2):
    total = num1 % num2
    return total